const suma = (val1, val2) => {
    return val1 + val2
}

const resta = (val1, val2)  => {
    return val1 - val2
}

const div = (val1, val2)  => {
    return val1 / val2
}

const mult = (val1, val2)  => {
    return val1 * val2
}

const resto = (val1, val2)  => {
    return val1 % val2
}

module.exports = {suma, resta, div, mult, resto}